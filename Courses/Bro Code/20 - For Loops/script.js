/* for loop = repeat some code a LIMITED amount of times */
/* there are upto three statements that are written in the for loop */

/* =================================== Increament ===================================== */

/* temporary counter (let i = 0),
condition to continue the loop (i <= 2).
increment/decrement (i++) */

for(let i = 0; i <= 2; i++){
    console.log("Hello");
}

/* =================================== Decreament ===================================== */

for(let i = 10; i > 0; i--){
    console.log(i);
}